# Diferentes formas de definir strings em Python
# Usando aspas simples, aspas duplas, aspas triplas simples e aspas triplas duplas

a = 'O autor disse assim: "A vida é bela!" e depois encerrou a palestra.'
print(a)

b = "O autor disse assim: 'A vida é bela!' e depois encerrou a palestra."
print(b)

c = '''O autor disse assim: "A vida é bela!" e depois encerrou a palestra.'''
print(c)

d = """O autor disse assim: 'A vida é bela!' e depois encerrou a palestra."""
print(d)

f = '''O autor disse assim: """A vida é bela!""" e depois encerrou a palestra.'''
print(f)